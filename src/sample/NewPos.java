package sample;

public class NewPos {

    private int oldPos;
    private int newPos;

    public NewPos(int oldPos, int newPos) {
        this.oldPos = oldPos;
        this.newPos = newPos;
    }

    public int getOldPos() {
        return oldPos;
    }

    public int getNewPos() {
        return newPos;
    }
}
